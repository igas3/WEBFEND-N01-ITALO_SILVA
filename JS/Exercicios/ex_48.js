let diaSemana = 3;
let mensagem = "";

switch (diaSemana){
    case 1: 
        mensagem = "Sunday";
        break;
    case 2: 
        mensagem = "Monday";
        break;
    case 3: 
        mensagem = "Tuesday";
        break;
    case 4: 
        mensagem = "Wednesday";
        break;
    case 5: 
        mensagem = "Thursday";
        break;
    case 6: 
        mensagem = "Friday";
        break;
    case 7:
        mensagem = "Saturday"
        break;
    default:
        mensagem = "Is not a day from week"
        break;
}